/*Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a 
área do retângulo.*/

let base = parseFloat(prompt("informe o valor da base do seu retângulo"));
let altura = parseFloat(prompt("informe o valor da altura do seu retângulo"));
let resultado = base * altura;
alert("A área do seu retangulo é: " + resultado);